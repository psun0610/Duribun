# PRD: 두리번 MVP

## 문제 정의

커플은 함께 다녀온 데이트 장소를 사적으로 기억하고 평가할 방법이 필요하다. 기존 공개 리뷰 서비스는 개인의 공개 리뷰에 최적화되어 있어, 두 사람이 각자 비공개 감상을 남기고 상대의 리뷰를 기다린 뒤 안전한 일부 정보만 선택적으로 공유하는 커플 중심 흐름을 지원하지 않는다.

## 해결 방향

비공개 커플 장소 리뷰와 친구 커플 및 공개 탐색으로의 선택적 공유를 지원하는 Next.js, TypeScript, Supabase 애플리케이션을 만든다.

MVP에는 소셜 인증, 개인 프로필, 커플 초대 코드, Kakao 장소 검색, 수동 장소 등록, 비공개 커플 리뷰, 필수 사진 업로드, 장소별 공유 설정, 친구 코드 기반 커플 친구 관계, 친구 커플 필터, 공개 탐색, 7일 유예 기간이 있는 커플 연결 해제가 포함된다.

## 수락 기준

- 사용자는 Kakao, Naver, Google로 인증할 수 있다.
- 사용자는 프로필을 만들 수 있다.
- 커플이 없는 사용자는 커플 초대 코드를 만들 수 있다.
- 커플이 없는 사용자는 초대 코드를 입력해 커플에 참여할 수 있다.
- 커플 초대 코드와 친구 코드는 별도의 개념이다.
- 커플은 Kakao Local 검색으로 장소를 등록할 수 있다.
- 검색으로 찾을 수 없는 장소는 수동으로 등록할 수 있다.
- 장소 카테고리는 식당, 카페, 활동을 사용한다.
- 각 파트너는 커플 장소마다 하나의 리뷰를 작성할 수 있다.
- 리뷰에는 평점, 태그, 한 줄 리뷰, 사진 1장 이상이 필요하다.
- 모든 사진은 장소/음식 또는 커플/비공개로 분류된다.
- UI는 두 사람 모두 미작성, 본인만 작성, 상대만 작성, 둘 다 작성 상태에 맞는 리뷰 상태 문구를 보여준다.
- 커플 장소는 공개 또는 비공개로 설정할 수 있다.
- 커플 장소는 두 리뷰가 모두 완료되고 장소/음식 사진이 1장 이상 있을 때만 공개 가능하다.
- 공개 영역에는 장소명, 카테고리, 평균 평점, 태그, 장소/음식 사진만 노출된다.
- 한 줄 리뷰, 개인별 평점, 커플/비공개 사진은 비공개로 유지된다.
- 커플은 친구 코드를 복사하고 재생성할 수 있다.
- 다른 커플의 친구 코드를 입력하면 즉시 커플 간 친구 관계가 생성된다.
- 친구 추천은 친구 커플의 공개 가능한 장소를 보여준다.
- 친구 추천은 친구 커플 이름을 보여준다.
- 친구 커플 필터 토글은 개인 사용자별로 저장된다.
- 탐색은 모든 사용자의 공개 가능한 장소를 보여준다.
- 수동 등록 장소는 탐색에 노출되기 전에 승인 또는 Kakao 매칭이 필요하다.
- 어느 파트너든 커플 연결 해제를 요청할 수 있다.
- 연결 해제 대기 상태는 즉시 커플 공간을 잠그고 친구/탐색 노출을 중지한다.
- 어느 파트너든 7일 안에 연결 해제를 취소할 수 있다.
- 연결 해제 대기 만료 커플은 7일 후 삭제된다.
- 프론트엔드 필터링이 잘못되어도 데이터베이스 정책이 비공개 데이터 유출을 막는다.

## 구현 결정

- Next.js, TypeScript, Supabase Auth, Supabase Postgres, Supabase Storage, Kakao Local API로 구축한다.
- 전역 장소 레코드와 커플별 장소 레코드를 사용한다.
- 개인 리뷰를 저장하고 공개 영역에는 평균 평점만 노출한다.
- 공유 가능 여부를 데이터베이스 함수와 타입이 지정된 애플리케이션 헬퍼에 중앙화한다.
- 커플 생성, 초대 코드 참여, 친구 코드 등록, 친구 코드 재생성, 연결 해제 요청, 연결 해제 취소, 만료된 연결 해제 정리는 서버 측 RPC 작업으로 처리한다.
- 친구 추천과 탐색 읽기 모델을 분리한다.
- 필수 리뷰 사진은 Supabase Storage를 사용하고, 사진 유형과 장소 공개 상태에 따라 가시성을 강제한다.

## 테스트 결정

- 외부 동작과 권한 경계를 테스트한다.
- RLS, RPC 흐름, 공유 가능 여부, 스토리지 가시성에 대한 데이터베이스 테스트를 추가한다.
- 온보딩, 장소 등록, 리뷰 폼 검증, 사진 분류, 공유 토글, 친구 필터, 탐색 필터링, 연결 해제 잠금 상태에 대한 앱 수준 테스트를 추가한다.

## 범위 제외

- 장소별 여러 방문 기록.
- 공개 서술형 리뷰.
- 사진 편집 또는 앨범.
- 친구 요청 승인.
- 네이티브 모바일 앱.
- 고도화된 추천 개인화.

